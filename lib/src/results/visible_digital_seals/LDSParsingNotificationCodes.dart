  static LDSParsingNotificationCodes? getByValue(int? i) {
    if (i == null) return null;
    try {
      return LDSParsingNotificationCodes.values.firstWhere((x) => x.value == i);
    } catch (_) {
      return LDSParsingNotificationCodes.UNKNOWN;
    }
  }
